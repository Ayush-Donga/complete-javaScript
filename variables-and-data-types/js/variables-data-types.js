// JavaScript Variables and Data Types

// String - A sequence of characters
var name = "John"; 
console.log("Name (String):", name);

// Number - Integer or floating-point number
let age = 25;
console.log("Age (Number):", age);

// Boolean - Represents true or false
const isStudent = true; 
console.log("Is Student? (Boolean):", isStudent);

// Null - Represents an intentional absence of any value
let car = null;
console.log("Car (Null):", car);

// Undefined - Variable is declared but not assigned a value
let score;
console.log("Score (Undefined):", score);

// Object - Stores multiple values in key-value pairs
let person = {
  firstName: "John",
  lastName: "Doe",
  age: 25,
};
console.log("Person (Object):", person);

// Array - Stores multiple values in an ordered list
let colors = ["Red", "Green", "Blue"];
console.log("Colors (Array):", colors);
