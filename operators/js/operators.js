// Arithmetic Operators
let x = 10;
let y = 5;

console.log("Addition:", x + y); // 15
console.log("Subtraction:", x - y); // 5
console.log("Multiplication:", x * y); // 50
console.log("Division:", x / y); // 2
console.log("Modulus:", x % y); // 0

// Comparison Operators
console.log("Is x greater than y?", x > y); // true
console.log("Is x equal to y?", x == y); // false
console.log("Is x strictly equal to y?", x === y); // false (Different data types would also matter)
console.log("Is x not equal to y?", x != y); // true

// Logical Operators
console.log("Logical AND:", x > 5 && y < 10); // true
console.log("Logical OR:", x > 10 || y < 10); // true
console.log("Logical NOT:", !(x > y)); // false
